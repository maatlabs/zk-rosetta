//! Shared test-vector format and loader for the zk-rosetta parity harness.
//!
//! Vectors live as TOML under `vectors/<name>/vector.toml`, each deserialized into
//! a [`Vector`]: a verifying key, a proof, and its public inputs in an
//! ecosystem-neutral encoding (see `vectors/README.md`). [`validate()`] checks a
//! vector's structure; the proof itself is verified by external, audited adapters,
//! never here.

mod load;
mod model;
mod validate;

#[cfg(feature = "evm")]
pub mod evm;
#[cfg(feature = "filecoin")]
pub mod filecoin;
#[cfg(feature = "svm")]
pub mod svm;

